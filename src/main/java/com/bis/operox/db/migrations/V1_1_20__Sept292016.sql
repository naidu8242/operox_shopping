DROP TABLE IF EXISTS `operox`.`TICKET`;
CREATE TABLE `operox`.`TICKET` (
  ID BIGINT NOT NULL AUTO_INCREMENT,
  SUBJECT VARCHAR(500) DEFAULT NULL,
  SOURCE_TYPE VARCHAR(500) DEFAULT NULL,
  STORE_ID BIGINT,
  DEPARTMENT_ID BIGINT,
  TICKET_STATUS VARCHAR(20) DEFAULT NULL,
  PRIORITY  VARCHAR(20) DEFAULT NULL,
  CUSTOMER_ID BIGINT,
  FILE_CONTENT_TYPE VARCHAR(50),
  FILE_NAME text DEFAULT NULL,
  DOC_FILE MEDIUMBLOB,
  MESSAGE TEXT DEFAULT NULL,
  TICKET_CREATED_BY DATE,
  ISSUE_DATE DATE,
  DUE_DATE DATE,
  ASSIGN_TO BIGINT,
  STATUS INT,
  CREATED_DATE DATE,
  CREATED_BY VARCHAR(10),
  UPDATED_DATE DATE,
  UPDATED_BY VARCHAR(10),
  FOREIGN KEY(STORE_ID) REFERENCES `operox`.`STORE`(ID),
  FOREIGN KEY(DEPARTMENT_ID) REFERENCES `operox`.`DEPARTMENT`(ID),
  FOREIGN KEY(CUSTOMER_ID) REFERENCES `operox`.`CUSTOMER`(ID),
  FOREIGN KEY(ASSIGN_TO) REFERENCES `operox`.`USER`(ID),
  PRIMARY KEY (`ID`) 
); 
